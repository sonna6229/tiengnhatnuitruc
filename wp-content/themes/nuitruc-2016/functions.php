//get course list by slug
function get_course_action()
{
    $slug = $_POST['slug'];
    $args = [
        'category_name' => $slug,
        'order' => 'DESC',
        'post_status' => 'publish'
    ];
    $data = [];
    $query = new WP_Query($args);

    if( $query->have_posts() ){
        while($query->have_posts()) : $query->the_post();

            array_push($data,[
                'title' => get_the_title(),
                'thumbnail' => has_post_thumbnail() ? get_the_post_thumbnail(get_the_ID(),'newmedium-200x124') : get_template_directory_uri()."/images/no_image.png",
                'excerpt' => get_the_excerpt(),
                'permalink' => get_the_permalink()
            ]);
        endwhile;
        wp_reset_postdata();
    }

    if($query->have_posts())
    {
        die(json_encode($data));
    }else{
        die('Hiện tại không có khóa học nào .');
    }
}
